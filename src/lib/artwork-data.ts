  // Get all artwork series
  export function getAllArtworkSeries() {
    return artworkSeries
  }
  
  // Get a specific artwork series by slug
  export function getArtworkSeriesBySlug(slug: string) {
    return artworkSeries.find((series) => series.slug === slug)
  }
  
  // Get a specific artwork by series ID and artwork ID
  export function getArtworkById(seriesId: number, artworkId: number) {
    const series = artworkSeries.find((s) => s.id === seriesId)
    if (!series) return null
  
    return series.artworks.find((artwork) => artwork.id === artworkId)
  }
  
  // Get other artworks in the same series (excluding the current one)
  export function getOtherArtworksInSeries(seriesId: number, artworkId: number) {
    const series = artworkSeries.find((s) => s.id === seriesId)
    if (!series) return []
  
    return series.artworks.filter((artwork) => artwork.id !== artworkId)
  }
  
